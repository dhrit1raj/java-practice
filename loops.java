public class loops {
    public static void main(String[] args) {
     
        for (int i=1; i<=20; i++) {
   System.out.println(i);
    }

    int i = 0;
while(i<=20) {
   System.out.println(i);
   i++;
}
    
    int j = 0;
do {
   System.out.println(j);
   j++;
} while(j<=20);


    }
}
